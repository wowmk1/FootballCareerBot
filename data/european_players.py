# Helper functions
def get_all_teams():
    """Returns a list of all team keys"""
    return list(TEAM_ROSTERS.keys())

def get_teams_by_league(league):
    """Returns teams filtered by league (Champions League or Europa League)"""
    return {k: v for k, v in TEAM_ROSTERS.items() if v['league'] == league}

def get_teams_by_country(country):
    """Returns teams filtered by country"""
    return {k: v for k, v in TEAM_ROSTERS.items() if v['country'] == country}

def get_total_player_count():
    """Returns total number of players across all teams"""
    return sum(len(team['players']) for team in TEAM_ROSTERS.values())

def get_player_by_name(player_name):
    """Find a player by name across all teams"""
    for team_key, team_data in TEAM_ROSTERS.items():
        for player in team_data['players']:
            if player['name'].lower() == player_name.lower():
                return {**player, 'team': team_data['name']}
    return None

