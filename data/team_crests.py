def get_team_crest_url(team_id):
    """
    Get crest URL for a team
    Returns a cached URL or a placeholder if team not found
    """
    # Try to get from our cache
    crest = ALL_TEAM_CRESTS.get(team_id)
    
    if crest:
        return crest
    
    # Fallback to a nice placeholder with team name
    team_name = team_id.replace('_', ' ').title()
    return f"https://ui-avatars.com/api/?name={team_id.replace('_', '+')}&background=0f3460&color=fff&size=128&bold=true&rounded=true"


def get_competition_logo(competition_name):
    """
    Get competition logo URL
    """
    return COMPETITION_LOGOS.get(competition_name, '')


def get_all_cached_teams():
    """
    Get list of all teams with cached crests
    """
    return list(ALL_TEAM_CRESTS.keys())


def is_crest_cached(team_id):
    """
    Check if a team has a cached crest
    """
    return team_id in ALL_TEAM_CRESTS
